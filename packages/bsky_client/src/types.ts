///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
//	Bluesky API Types
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

export interface BlueskyPost {
	uri: string;
	cid: string;
	author: {
		did: string;
		handle: string;
		displayName?: string;
		avatar?: string;
	};
	record: {
		text: string;
		createdAt: string;
		reply?: {
			root: {
				uri: string;
				cid: string;
			};
			parent: {
				uri: string;
				cid: string;
			};
		};
	};
	indexedAt: string;
	likeCount: number;
	replyCount: number;
	repostCount: number;
}

export interface BlueskyProfile {
	did: string;
	handle: string;
	displayName?: string;
	description?: string;
	avatar?: string;
	banner?: string;
	followsCount: number;
	followersCount: number;
	postsCount: number;
	viewer?: {
		muted: boolean;
		blockedBy: boolean;
		blocking: boolean;
		following: boolean;
		followedBy: boolean;
	};
}

export interface BlueskyFeedGenerator {
	uri: string;
	cid: string;
	did: string;
	creator: {
		did: string;
		handle: string;
		displayName?: string;
	};
	displayName: string;
	description?: string;
	likeCount: number;
	indexedAt: string;
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////
//	Session Management Types
///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

export interface AuthSession {
	did: string;
	handle: string;
	accessJwt: string;
	refreshJwt: string;
}

export interface StoredSession extends AuthSession {
	createdAt: string;
	lastUsed: string;
}

